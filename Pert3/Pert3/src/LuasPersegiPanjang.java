public class LuasPersegiPanjang {

    public static void main(String[] args) {
        double panjang, lebar; // inisialisasi
        panjang = 10.00; // mengubah nilai variabel panjang menjadi 10
        lebar = 8.00; // mengubah nilai panjang menjadi 8
        System.out.println("Luas Persegi Panjang adalah " + (panjang * lebar));
    }
}